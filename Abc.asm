exitError:
	# assumes $a0 has pointer to null-terminated string

	# print the string
	li $v0,4 # syscall-code for print-string
	syscall
exit:
	# print termination message
	li $v0,4
	la $a0,exitString
	syscall

	# terminate execution
	li $v0,10 #syscall-code for "exit"
	syscall

outOfHeapMemory:
	la $a0,heapMemoryMsg
	j exitError
